package com.sp.pay;

public class Discount {
	String dcWay;
	int dcPay;
	int payCode;

	public String getDcWay() {
		return dcWay;
	}

	public void setDcWay(String dcWay) {
		this.dcWay = dcWay;
	}

	public int getDcPay() {
		return dcPay;
	}

	public void setDcPay(int dcPay) {
		this.dcPay = dcPay;
	}

	public int getPayCode() {
		return payCode;
	}

	public void setPayCode(int payCode) {
		this.payCode = payCode;
	}

}
